/**
 * Fetches all records from cghs_surgery table using pagination.
 */
async function fetchAllRecords() {
  const PAGE_SIZE = 1000;
  let allRecords = [];
  let page = 0;
  let hasMore = true;

  while (hasMore) {
    const from = page * PAGE_SIZE;
    const to = from + PAGE_SIZE - 1;

    const { data, error } = await supabase
      .from('cghs_surgery')
      .select('id, name, code, NABH_NABL_Rate')
      .range(from, to);

    if (error) {
      console.error(`Error fetching page ${page}:`, error.message);
      throw error;
    }

    if (data && data.length > 0) {
      allRecords = allRecords.concat(data);
      if (data.length < PAGE_SIZE) {
        hasMore = false;
      }
    } else {
      hasMore = false;
    }

    page++;
  }

  console.log(`Fetched ${allRecords.length} total records from cghs_surgery table.`);
  return allRecords;
}

/**
 * Main function: matches PDF entries against DB records by name (case-insensitive,
 * trimmed), then updates code and NABH_NABL_Rate for matched records.
 */
async function main() {
  console.log('=== CGHS Rate Update Script ===');
  console.log(`PDF entries to process: ${PDF_DATA.length}`);
  console.log('');

  console.log('Fetching all existing cghs_surgery records...');
  const existingRecords = await fetchAllRecords();
  console.log('');

  // Build a lookup map of lowercase trimmed name -> record
  const nameToRecord = new Map();
  for (const record of existingRecords) {
    if (record.name) {
      const key = record.name.trim().toLowerCase();
      nameToRecord.set(key, record);
    }
  }

  let matchedCount = 0;
  let updatedCount = 0;
  const unmatchedPdfEntries = [];
  const updateErrors = [];

  for (const pdfEntry of PDF_DATA) {
    const pdfKey = pdfEntry.name.trim().toLowerCase();
    const dbRecord = nameToRecord.get(pdfKey);

    if (!dbRecord) {
      unmatchedPdfEntries.push(pdfEntry);
      continue;
    }

    matchedCount++;

    const newRate = parseFloat(pdfEntry.rate);
    const { error } = await supabase
      .from('cghs_surgery')
      .update({
        code: pdfEntry.code,
        NABH_NABL_Rate: isNaN(newRate) ? null : newRate
      })
      .eq('id', dbRecord.id);

    if (error) {
      updateErrors.push({ name: pdfEntry.name, error: error.message });
      console.error(`  ERROR updating "${pdfEntry.name}": ${error.message}`);
    } else {
      updatedCount++;
    }
  }

  console.log('');
  console.log('=== UPDATE REPORT ===');
  console.log(`Total PDF entries:      ${PDF_DATA.length}`);
  console.log(`Total DB records:       ${existingRecords.length}`);
  console.log(`Matched:                ${matchedCount}`);
  console.log(`Successfully updated:   ${updatedCount}`);
  console.log(`Update errors:          ${updateErrors.length}`);
  console.log(`Unmatched PDF entries:  ${unmatchedPdfEntries.length}`);
  console.log('');

  if (updateErrors.length > 0) {
    console.log('--- Update Errors ---');
    for (const err of updateErrors) {
      console.log(`  "${err.name}": ${err.error}`);
    }
    console.log('');
  }

  if (unmatchedPdfEntries.length > 0) {
    console.log('--- Unmatched PDF Entries (not found in DB) ---');
    for (const entry of unmatchedPdfEntries) {
      console.log(`  [${entry.code}] ${entry.name} (Rate: ${entry.rate})`);
    }
    console.log('');
  }

  console.log('=== Script Complete ===');
}

main().catch((err) => {
  console.error('Fatal error:', err);
  process.exit(1);
});
